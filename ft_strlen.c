#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	n;

	n = 0;
	while (s[n] != '\0')
		n++;
	return (n);
}
// ft_strlen  | Calcula o comprimento de uma string (sem contar o `\0`). |
